package PierwszaPaczka;

public class testOpertory {

	public static void main(String[] args) {
		
		int a = 17;
		int b = 4;
		double c = a+b;
		System.out.println(c);
		c = a-b;
		System.out.println(c);
		c = a*b;
		System.out.println(c);
		c = a/b;
		System.out.println(c);
		a /= b;
		System.out.println(a);
		a += b;
		System.out.println(a);
		a -= b;
		System.out.println(a);
		a *= b;
		a++;
		System.out.println(a);
		System.out.println(a++);
		System.out.println(++a);
		System.out.println(a--);
		System.out.println(--a);
	}

}