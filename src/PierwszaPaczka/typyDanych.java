package PierwszaPaczka;

public class typyDanych {
	
	public static void main(String[] args){
		float liczbaFloat = 25.75f;
		int liczbaInt = 25;
		char znakA = 'A';
		boolean czyUczProg = false;
		
		System.out.println("Liczba int: ");
		System.out.println(liczbaInt);
		System.out.println(25);
		System.out.println("Liczba float: ");
		System.out.println(liczbaFloat);
		System.out.println(25.75f);
		System.out.println("Znak A: ");
		System.out.println(znakA);
		System.out.println('A');
		System.out.println("Czy uczysz sie programowania: ");
		System.out.println(czyUczProg);
		System.out.println(true);
	}
	

}